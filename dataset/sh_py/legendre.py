import torch
from math import sqrt


def _machine_precision_root_bisect(xl, xr, sl, sr, sign_fn):
  assert sl == -sr and sl != 0
  while True:
    xm = 0.5 * (xl + xr)
    if xm == xl or xm == xr:
      return xm
    sm = sign_fn(xm)
    if sm == 0:
      return xm
    if sm == sl:
      xl = xm
    else:
      xr = xm


class SymmetricPolynomialRootRepresentation:
  """
  Polynomial representation for symmetric polynomials that explots that any axis-symmetric polynomial decomposes
  into factors (x^2-a) and any rotation-symmetric polynomial is an axis-symmetric one times the factor x.
  """

  def __init__(self, c, squared_roots, rotation_symmetric, dtype=None, device=None):
    self.__squared_roots = torch.as_tensor(squared_roots, dtype=dtype, device=device)
    self.__c = torch.as_tensor(c, dtype=self.__squared_roots.dtype, device=self.__squared_roots.device)
    self.__rotation_symmetric = rotation_symmetric

  def _as_tensor(self, x):
    return torch.as_tensor(x, dtype=self.__c.dtype, device=self.__c.device)

  @property
  def squared_roots(self):
    return tuple(r.item() for r in self.__squared_roots)

  @property
  def leading_coeficient(self):
    return self.__c.item()

  @property
  def rotation_symmetric(self):
    return self.__rotation_symmetric

  def to(self, dtype=None, device=None):
    self.__c = self.__c.to(dtype=dtype, device=device)
    self.__squared_roots = self.__squared_roots.to(dtype=dtype, device=device)

  def __call__(self, x):
    x = self._as_tensor(x)
    x2 = x * x
    factors = x2[..., None] - self.__squared_roots
    y = self.__c * torch.prod(factors, dim=-1)
    if self.__rotation_symmetric:
      y *= x
    return y


class ScaledLegendrePolynomialFactory:
  def __init__(self, dtype=None, device=None):
    self.__dtype = dtype
    self.__device = device
    self.__cache = {}
    for (l, m), values in LEGENDRE_LEADING_COEFICIENT_AND_SQUARED_ROOTS.items():
      p = SymmetricPolynomialRootRepresentation(values[0], values[1:],
                                                rotation_symmetric=(l - m) % 2 == 1,
                                                dtype=dtype, device=device)
      self.__cache[l, m] = p

  def print_cache_dict_string(self):
    print("LEGENDRE_LEADING_COEFICIENT_AND_SQUARED_ROOTS = {")
    for k in sorted(self.__cache):
      p = self.__cache[k]
      values = (p.leading_coeficient,) + p.squared_roots
      print(f"  {k}: {values},")
    print("}")

  def populate_cache(self, max_degree):
    for m in range(0, max_degree + 1):
      self.get(max_degree, m)

  def get(self, l, m):
    if not (l, m) in self.__cache:
      self.__cache[l, m] = self.__build(l, m)
    return self.__cache[l, m]

  def __build(self, l, m):
    if l < 0 or m < 0 or m > l:
      raise ValueError("l,m indicies do not describe an associated legendre polynomial.")

    if l == 0 and m == 0:
      c = 0.28209479177387814  # = 0.5 * np.sqrt(1 / np.pi)
      return SymmetricPolynomialRootRepresentation(c, [], False, self.__dtype, self.__device)
    elif l == m:
      p = self.get(m - 1, m - 1)
      assert len(p.squared_roots) == 0
      c2 = - sqrt((2 * m + 1) / (2 * m))
      return SymmetricPolynomialRootRepresentation(c2 * p.leading_coeficient, [], False, self.__dtype, self.__device)
    elif l == m + 1:
      p = self.get(m, m)
      assert len(p.squared_roots) == 0
      assert not p.rotation_symmetric
      c2 = sqrt(2 * m + 3)
      return SymmetricPolynomialRootRepresentation(c2 * p.leading_coeficient, [], True, self.__dtype, self.__device)
    else:
      p1 = self.get(l - 1, m)
      p2 = self.get(l - 2, m)
      c1 = sqrt((4 * l * l - 1) / (l * l - m * m))
      c2 = -sqrt((2 * l + 1) / (2 * l - 3) * ((l - 1) * (l - 1) - m * m) / (l * l - m * m))
      rotation_symmetric = (l - m) % 2 == 1
      assert p1.rotation_symmetric != rotation_symmetric
      assert p2.rotation_symmetric == rotation_symmetric

      w1 = c1 * p1.leading_coeficient
      w2 = c2 * p2.leading_coeficient
      n_expected_roots = (l - m) // 2
      if rotation_symmetric:
        squared_roots = self.__find_squared_roots(p1.squared_roots, p2.squared_roots, w1, w2, n_expected_roots)
      else:
        squared_roots = self.__find_squared_roots((0,) + p1.squared_roots, p2.squared_roots, w1, w2, n_expected_roots)
      return SymmetricPolynomialRootRepresentation(w1, squared_roots, rotation_symmetric, self.__dtype, self.__device)

  def __find_squared_roots(self, r1, r2, w1, w2, n_expected_roots):
    """Exploit the fact the legrende polynomials have distinct roots that are located in (-1,1), or rather in [0,1)
    after substituting x -> x * x"""
    r1 = torch.as_tensor(r1, dtype=self.__dtype)
    r2 = torch.as_tensor(r2, dtype=self.__dtype)

    def sign_fn(x):
      y = w1 * torch.prod(x[..., None] - r1, dim=-1) + w2 * torch.prod(x[..., None] - r2, dim=-1)
      return torch.sign(y)

    for i in range(10, 18):
      x2 = torch.arange(0, 1, 2 ** -i, dtype=self.__dtype)
      signs = sign_fn(x2)
      sign_zero_mask: torch.Tensor = signs[1:] == 0

      sign_change_mask = ~sign_zero_mask
      sign_change_mask &= signs[1:] == -signs[:-1]

      n_roots = torch.sum(sign_zero_mask) + torch.sum(sign_change_mask)
      if n_expected_roots != n_roots:
        continue

      roots = []
      for j in torch.where(sign_zero_mask)[0]:
        roots.append(x2[j])
      for j in torch.where(sign_change_mask)[0]:
        root = _machine_precision_root_bisect(x2[j], x2[j + 1], signs[j], signs[j + 1], sign_fn)
        roots.append(root)
      return roots
    raise RuntimeError("Cannot compute legendre root representation. Did not find enough intervals containing roots.")


